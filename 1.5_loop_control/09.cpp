/*题目：
    计算非负整数m到n(包括m和n)之间的所有奇数的和,其中m不大于n,且n不大于300。例如:m=3,n=12,其和则为:3+5+7+9+11=35。
输入:
    两个数m和n,两个数以一个空格分开,其中0<=m<=n<=300 。
输出:
    输出一行,包含一个整数,表示m到n(包括m和n)之间的所有奇数的和。*/
#include <stdio.h>

int main() {
    int m, n, sum = 0;
    scanf("%d %d", &m, &n);
    if (m % 2 == 0) {
        m++;
    }
    if (n % 2 == 0) {
        n -= 1;
    }
    for (m; m <= n; m++) {
        sum += m;
        m++;
    }
    printf("%d", sum);
    return 0;
}
